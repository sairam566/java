package assigmnent1;

public class TriangleAreaAndPerimeter
{
	public static void main(String[] args)
	{	
		int radious=2;
		float PI=3.14f;
		
		float area,perimeter;
		
		area=PI*radious*radious;
		
		perimeter=2*PI*radious;
		
		System.out.println("The Area of a Circle with Radious "+radious+" is :"+area);
		
		System.out.println("The Perimeter of a Circle with Radious "+radious+" is :"+perimeter);
	}

}
