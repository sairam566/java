package assigmnent1;

public class AverageOfThreeNo
{
	public static void main(String[] args)
	{
		int value1=10;
		int value2=20;
		int value3=30;
		
		int total,average;
		
		total=value1+value2+value3;
		average=total/3;
		
		System.out.println("The Total of "+value1+" + "+value2+" + "+value3+"  is :"+total);
		System.out.println("The Average of "+value1+" , "+value2+" , "+value3+" is :"+average);
	}

}
