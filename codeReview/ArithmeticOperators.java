package assigmnent1;

public class ArithmeticOperators
{
	public static void main(String[] args)
	{	
		int value1 = 10;
		int value2 = 20;
		
		int addition , subraction , division , multiplication;
		
		addition = value1 + value2;
		
		subraction = value1 - value2;
		
		division = value1 / value2;
		
		multiplication = value1 * value2;
		
		System.out.println("The Addition of       "+value1+" + "+value2+" = "+addition);
		System.out.println("The Subraction of     "+value1+" - "+value2+" = "+subraction);
		System.out.println("The Division of       "+value1+" / "+value2+" = "+division);
		System.out.println("The Multiplication of "+value1+" * "+value2+" = "+multiplication);
	}
}
